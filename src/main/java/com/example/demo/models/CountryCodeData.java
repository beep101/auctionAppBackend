package com.example.demo.models;

public class CountryCodeData {
	private String name;
	private String len2;
	private String len3;
	private String num;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getLen2() {
		return len2;
	}
	public void setLen2(String len2) {
		this.len2 = len2;
	}
	public String getLen3() {
		return len3;
	}
	public void setLen3(String len3) {
		this.len3 = len3;
	}
	public String getNum() {
		return num;
	}
	public void setNum(String num) {
		this.num = num;
	}
	
	
}
